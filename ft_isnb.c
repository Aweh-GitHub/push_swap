#include <stddef.h>

int	ft_isnb(char *s)
{
	size_t	i;

	if (s == NULL)
		return (0);
	i = 0;
	if (s[0] == '-' || s[0] == '+')
	{
		if (s[1] >= '0' && s[1] <= '9')
			i++;
		else
			return (0);
	} 
	while (s[i])
	{
		if (!(s[i] >= '0' && s[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}